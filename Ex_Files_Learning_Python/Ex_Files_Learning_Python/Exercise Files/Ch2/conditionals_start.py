# Example file for working with conditional statements
#

def main():
  x, y = 1000, 1000

  if (x < y):
    st = "X is less then  Y"

  elif (x == y):
    st = "X is equals to Y"

  else:
    st = "X is greater than Y"

  print(st)
  # conditional flow uses if, elif, else  

  # conditional statements let you use "a if C else b"
  

if __name__ == "__main__":
  main()
